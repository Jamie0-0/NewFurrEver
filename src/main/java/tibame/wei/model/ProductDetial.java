package tibame.wei.model;

public class ProductDetial {
	private Integer p_id;
	private String p_name;
	private Integer p_m_price;
	private Integer p_m_stock;
	public Integer getP_id() {
		return p_id;
	}
	public void setP_id(Integer p_id) {
		this.p_id = p_id;
	}
	public String getP_name() {
		return p_name;
	}
	public void setP_name(String p_name) {
		this.p_name = p_name;
	}
	public Integer getP_m_price() {
		return p_m_price;
	}
	public void setP_m_price(Integer p_m_price) {
		this.p_m_price = p_m_price;
	}
	public Integer getP_m_stock() {
		return p_m_stock;
	}
	public void setP_m_stock(Integer p_m_stock) {
		this.p_m_stock = p_m_stock;
	}
}
