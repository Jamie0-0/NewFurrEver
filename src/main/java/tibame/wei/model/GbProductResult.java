public class GbProductResult {
    private String p_name;
    private int gb_c_max;
    private int gb_s_price;
    private String p_type;
    private int people;
	public String getP_name() {
		return p_name;
	}
	public void setP_name(String p_name) {
		this.p_name = p_name;
	}
	public int getGb_c_max() {
		return gb_c_max;
	}
	public void setGb_c_max(int gb_c_max) {
		this.gb_c_max = gb_c_max;
	}
	public int getGb_s_price() {
		return gb_s_price;
	}
	public void setGb_s_price(int gb_s_price) {
		this.gb_s_price = gb_s_price;
	}
	public String getP_type() {
		return p_type;
	}
	public void setP_type(String p_type) {
		this.p_type = p_type;
	}
	public int getPeople() {
		return people;
	}
	public void setPeople(int people) {
		this.people = people;
	}
}
